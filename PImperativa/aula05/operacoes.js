/* Atribuição
**/

//variavel
let idade = 40; //atribui nummero 40 a idade
console.log(idade);

/* Numéricos
**/

let soma = 2 + 5; //soma -> 7
console.log(soma);

let subtracao = 10 - 5; //subtracao -> 5
console.log(subtracao);

let multiplicacao = 3 * 5;  //multiplicação -> 15
console.log(multiplicacao);

let divisao = 15 / 3;  // divisao -> 5
console.log(divisao);

/* Aritméticos
**/

let incrementoA = 15;
console.log(incrementoA++);  //incrementa 1 na variavel mas retorna valor anterior a modificacao -> 15
console.log(incrementoA); 

let incrementoB = 15;
console.log(++incrementoB);   //incrementa 1 na variavel e retorna novo valor -> 16
console.log(incrementoB); 

let decrementoA = 10;
console.log(decrementoA++);  //decrementa 1 na variavel mas retorna valor anterior a modificacao -> 10
console.log(decrementoA); 

let decrementoB = 10;
console.log(++decrementoB);   //decrementa 1 na variavel e retorna novo valor -> 9
console.log(decrementoB); 

let modulo = 10 % 3;  //retorno a resto da divisao entre 10 e 3 -> 1
console.log(modulo);  

/* Concatenação
**/

let nome = "Jimi";
let sobrenome = "Hendrix";
let nomeCompleto = nome + ' ' + sobrenome;  // Jimi Hendrix
console.log(nomeCompleto);

let fileira = 'M';
let assento = 7;
let local = fileira + ' ' + assento;  // "M7" como uma string
console.log(local);

